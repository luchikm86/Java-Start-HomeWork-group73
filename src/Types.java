public class Types {
    public static void main(String[] args) {
        //Primitives


        //Integer
        byte b = 100; // 1b
        short s = 10000; // 2 b
        int x = 100000000; // 4 b (default)
        long l = 100000000000000000L;   // 8 b

        // Floating point types
        float f = 10.5f; // 4b
        double d = 10.5; // 8 b (default)

        //Character
        char c = '1'; // 2b, UNICODE

        // logic
        boolean bool = true; // или false

        // object types
        String str = "Hello";
    }
}
